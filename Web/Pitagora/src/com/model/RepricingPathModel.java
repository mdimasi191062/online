package com.model;

public class RepricingPathModel {

    private String pathStorico;
    private String code;
    private String pathZip;
    private String descr;
    private String extFile;
    private String extFileStorico;

    public void setPathStorico(String pathStorico) {
        this.pathStorico = pathStorico;
    }

    public String getPathStorico() {
        return pathStorico;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getCode() {
        return code;
    }

    public void setPathZip(String pathZip) {
        this.pathZip = pathZip;
    }

    public String getPathZip() {
        return pathZip;
    }

    public void setDescr(String descr) {
        this.descr = descr;
    }

    public String getDescr() {
        return descr;
    }

    public void setExtFile(String extFile) {
        this.extFile = extFile;
    }

    public String getExtFile() {
        return extFile;
    }

    public void setExtFileStorico(String extFileStorico) {
        this.extFileStorico = extFileStorico;
    }

    public String getExtFileStorico() {
        return extFileStorico;
    }
}
