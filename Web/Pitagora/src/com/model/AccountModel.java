package com.model;

public class AccountModel {
    private String codeTipoContr;
    private String codeAccount;
    private String descAccount;

    public void setCodeTipoContr(String codeTipoContr) {
        this.codeTipoContr = codeTipoContr;
    }

    public String getCodeTipoContr() {
        return codeTipoContr;
    }

    public void setCodeAccount(String codeAccount) {
        this.codeAccount = codeAccount;
    }

    public String getCodeAccount() {
        return codeAccount;
    }

    public void setDescAccount(String descAccount) {
        this.descAccount = descAccount;
    }

    public String getDescAccount() {
        return descAccount;
    }
}
